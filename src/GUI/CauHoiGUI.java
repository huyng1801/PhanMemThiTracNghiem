    private void loadDataToTable() {
        DefaultTableModel model = (DefaultTableModel) tableCauHoi.getModel();
        model.setRowCount(0);
        model.setColumnCount(0);
        model.addColumn("Mã câu hỏi");
        model.addColumn("Mã đề");
        model.addColumn("Câu hỏi");
        int maDe = DeThiGUI.maDeThiDuocChon;
        List<CauHoiDTO> lstCauHoi = CauHoiDAL.layDanhSachCauHoiTheoMaDe(maDe);

        for (CauHoiDTO ch : lstCauHoi) {
            Object[] rowData = {ch.getMaCauHoi(), ch.getMaDe(), ch.getCauHoiText()};
            model.addRow(rowData);
        }
    }

