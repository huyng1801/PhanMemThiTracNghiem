    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int maDeThi = Integer.parseInt(txtDeThi.getText().split("-")[0]);
        String cauHoi = txtCauHoiText.getText();
        String luaChonTextA = txtA.getText();
        boolean laLuaChonDungA = false;
        if (rbtnA.isSelected()) {
            laLuaChonDungA = true;
        }

        String luaChonTextB = txtB.getText();
        boolean laLuaChonDungB = false;
        if (rbtnB.isSelected()) {
            laLuaChonDungB = true;
        }

        String luaChonTextC = txtC.getText();
        boolean laLuaChonDungC = false;
        if (rbtnC.isSelected()) {
            laLuaChonDungC = true;
        }

        String luaChonTextD = txtD.getText();
        boolean laLuaChonDungD = false;
        if (rbtnD.isSelected()) {
            laLuaChonDungD = true;
        }
        if ("".equals(cauHoi)) {
            JOptionPane.showMessageDialog(this, "Câu hỏi không được bỏ trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if ("".equals(luaChonTextA) || "".equals(luaChonTextB) || "".equals(luaChonTextC) || "".equals(luaChonTextD)) {
            JOptionPane.showMessageDialog(this, "Nội dung lựa chọn không được bỏ trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        CauHoiDTO ch = new CauHoiDTO(maDeThi, cauHoi);
        if (CauHoiDAL.themCauHoi(ch)) {
            int maCauHoi = CauHoiDAL.layMaCauHoiLonNhat();
            if (maCauHoi != 0) {
                LuaChonDTO luaChonA = new LuaChonDTO(maCauHoi, luaChonTextA, laLuaChonDungA);
                LuaChonDTO luaChonB = new LuaChonDTO(maCauHoi, luaChonTextB, laLuaChonDungB);
                LuaChonDTO luaChonC = new LuaChonDTO(maCauHoi, luaChonTextC, laLuaChonDungC);
                LuaChonDTO luaChonD = new LuaChonDTO(maCauHoi, luaChonTextD, laLuaChonDungD);
                LuaChonDAL.themLuaChon(luaChonA);
                LuaChonDAL.themLuaChon(luaChonB);
                LuaChonDAL.themLuaChon(luaChonC);
                LuaChonDAL.themLuaChon(luaChonD);
            }
            JOptionPane.showMessageDialog(this, "Thêm câu hỏi thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            loadDataToTable();
            clearCauHoi();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm câu hỏi thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

