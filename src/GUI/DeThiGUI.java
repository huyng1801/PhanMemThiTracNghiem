    private void loadDataToTable() {
        DefaultTableModel model = (DefaultTableModel) tableDeThi.getModel();
        model.setRowCount(0);
        model.setColumnCount(0);
        model.addColumn("Mã đề thi");
        model.addColumn("Tên đề thi");
        model.addColumn("Tổng điểm");
        List<DeThiDTO> lstDeThi = DeThiDAL.layDanhSachDeThi();

        for (DeThiDTO dt : lstDeThi) {
            Object[] rowData = {dt.getMaDe(), dt.getTenDe(), dt.getTongDiem()};
            model.addRow(rowData);
        }
    }

