    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if ("".equals(txtTenDeThi.getText())) {
            JOptionPane.showMessageDialog(this, "Tên đề thi không được bỏ trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        float tongDiem = 0.0f;
        try {
            tongDiem = Float.parseFloat(txtTongDiem.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Tổng điểm phải là số thực!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String tenDeThi = txtTenDeThi.getText();
        DeThiDTO dt = new DeThiDTO(tenDeThi, tongDiem);
        if (DeThiDAL.themDeThi(dt)) {
            JOptionPane.showMessageDialog(this, "Thêm đề thi thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            loadDataToTable();
            xoaDuLieu();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm đề thi thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableDeThi.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một đề thi để sửa!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if ("".equals(txtTenDeThi.getText())) {
            JOptionPane.showMessageDialog(this, "Tên đề thi không được bỏ trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        float tongDiem = 0.0f;
        try {
            tongDiem = Float.parseFloat(txtTongDiem.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Tổng điểm phải là số thực!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int maDeThi = Integer.parseInt(txtMaDeThi.getText());
        String tenDeThi = txtTenDeThi.getText();
        DeThiDTO dt = new DeThiDTO(maDeThi, tenDeThi, tongDiem);
        if (DeThiDAL.capNhatDeThi(dt)) {
            JOptionPane.showMessageDialog(this, "Sửa đề thi thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            loadDataToTable();
            xoaDuLieu();
        } else {
            JOptionPane.showMessageDialog(this, "Sửa đề thi thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableDeThi.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một đề thi để xóa!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int maDeThi = Integer.parseInt(tableDeThi.getValueAt(selectedRow, 0).toString());
        int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa đề thi này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {
            if (DeThiDAL.xoaDeThi(maDeThi)) {

                JOptionPane.showMessageDialog(this, "Xóa đề thi thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadDataToTable();
                xoaDuLieu();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa đề thi thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed
    private void xoaDuLieu() {
        txtMaDeThi.setText("");
        txtTenDeThi.setText("");
        txtTongDiem.setText("");
    }
    private void tableDeThiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDeThiMouseClicked
        // TODO add your handling code here:
        int selectedRow = tableDeThi.getSelectedRow();
        if (selectedRow != -1) {
            String maDeThi = tableDeThi.getValueAt(selectedRow, 0).toString();
            String tenDeThi = tableDeThi.getValueAt(selectedRow, 1).toString();
            String tongDiem = tableDeThi.getValueAt(selectedRow, 2).toString();
            txtMaDeThi.setText(maDeThi);
            txtTenDeThi.setText(tenDeThi);
            txtTongDiem.setText(tongDiem);
        }
    }//GEN-LAST:event_tableDeThiMouseClicked

